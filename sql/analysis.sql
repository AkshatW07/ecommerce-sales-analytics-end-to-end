-- QUERY 1 :CATEGORY ANALYSIS

USE ecommerce_project;

-- checking which categories generate most values

SELECT P.CATEGORY,
COUNT(*) AS TOTAL_PRODUCTS,
AVG(PR.DISCOUNTED_PRICE) AS AVG_PRICE
FROM PRODUCTS P
JOIN PRICING PR
ON P.PRODUCT_ID = PR.PRODUCT_ID
GROUP BY P.CATEGORY
ORDER BY AVG_PRICE DESC;

-- JOINS BOTH TABLES
-- SHOWS : AVG PRICE AND NUMBER OF PRODUCTS

-- I used SQL joins to analyze category-level pricing and product distribution.

-- Certain categories dominate the product inventory, indicating focus areas for business expansion and competition


-- QUERY 2 : TOP BRANDS ACCORDING TO REVENUE
-- TELLS US ABOUT WHICH BRAND WILL HAVE THE HIGHEST PRICING POWER
SELECT P.BRAND,
COUNT(*) AS TOTAL_PRODUCTS,
AVG(PR.DISCOUNTED_PRICE) AS AVG_PRICE
FROM PRODUCTS P
JOIN PRICING PR
ON P.PRODUCT_ID = PR.PRODUCT_ID
GROUP BY P.BRAND
ORDER BY AVG_PRICE DESC
LIMIT 10;

-- HIGH AVG PRICE - PREMIUM BRAND
-- LOW AVG PRICE - BUDGET BRAND

-- INSIGHTS:
-- BRAND ANGELFISH SELLS FEWER BUT HAS HIGH-VALUE PRODUCTS
