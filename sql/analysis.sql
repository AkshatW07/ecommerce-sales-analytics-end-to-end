-- QUERY 3
-- TELLS US ABOUT WHICH CATEGORIES RELY HEAVILY ON DISCOUNT


 SELECT P.CATEGORY,
 AVG(PR.DISCOUNT_PERCENT) AS AVG_DISCOUNT,
 COUNT(*) AS TOTAL_PRODUCTS
 FROM PRODUCTS P
JOIN PRICING PR
ON P.PRODUCT_ID = PR.PRODUCT_ID
GROUP BY P.CATEGORY
ORDER BY AVG_DISCOUNT DESC;

-- HIGH DISCOUNTS = COMPETITIVE MARKET
-- LOW DISCOUNT - PREMIUM/MONOPOLY

-- INSIGHTS
-- CLOTHING BRANDS FOCUS MORE ON DISCOUNTS COMPARED TO OTHER BRNADS
-- OTHER BRANDS MAINTAIN A STABLE PRICE AND DISCOUNT MOSTLY BELOW 50%

-- Categories with higher average discounts indicate competitive markets where pricing strategies play a key role in sales.

-- QUERY 4 - PRICE V/S RATING


-- CHECKING THAT ONLY EXPENSIVE PRODUCTS HAVE BETTER RATINGS


SELECT 
      CASE
         WHEN PR.DISCOUNTED_PRICE < 1000 THEN "LOW"
         WHEN PR.DISCOUNTED_PRICE < 5000 THEN "MEDIUM"
         ELSE "HIGH"
     END AS PRICE_RANGE,
     AVG(P.PRODUCT_RATING) AS AVG_RATING,
     COUNT(*) AS TOTAL_PRODUCTS
FROM PRODUCTS P
JOIN PRICING PR
ON P.PRODUCT_ID = PR.PRODUCT_ID
GROUP BY PRICE_RANGE;

-- INSIGHTS
-- HIGH RANGE PRODUCTS NOT ALWAYS HAS HIGHEST RATINGS HIGH PRICE HAS AVG RATING OF 3.847368416033293

-- MID RANGE PRODUCTS HAVE THE HIGHEST RATINGS 3.914814820996037

-- Higher-priced products do not always guarantee better ratings, highlighting gaps in perceived value and customer satisfaction


